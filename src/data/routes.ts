// Navigation routes and AR step data for campus wayfinding

export interface NavStep {
  id: string
  instruction: string
  detail: string
  distance: string        // e.g. "15m"
  direction: 'straight' | 'left' | 'right' | 'up' | 'down' | 'arrive'
  landmark: string        // landmark to look for
  arrowAngle: number      // degrees for AR overlay arrow (0=up, 90=right, 180=down, 270=left)
  // In a real deployment, imageUrl points to an actual photo taken on campus
  imageUrl: string        // campus photo for this step
  floorChange?: {
    type: 'elevator' | 'stairs'
    from: number
    to: number
  }
  accessible: boolean     // can wheelchair user follow this step?
}

export interface Route {
  id: string
  fromId: string
  toId: string
  fromName: string
  toName: string
  totalDistance: string
  estimatedTime: string   // minutes
  accessibleTime: string  // minutes (accessible route may be longer)
  steps: NavStep[]
  accessibleSteps: NavStep[]
  trafficLevel: 'low' | 'medium' | 'high'  // based on time of day simulation
}

